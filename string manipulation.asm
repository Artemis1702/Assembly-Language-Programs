include "emu8086.inc"
ORG 100H
.DATA
STR DB 50,50 DUP(?)
STRLEN1 DB 0
STRLEN DW 0
CHOICE DW 0 
VOWLOC DW 0
PIG DB "AY$"
MSG1 DB "Enter A String: $" 
NLINE DB 13,10,"$"
MSG2 DB "0.Re-Enter String",13,10,"1.Count The Number Of Vowels Of The Given String",13,10,"2.Print The Vowels Of The Given String",13,10,"3.Print The Piglatin Form Of The Word",13,10,"4.Convert To Uppercase",13,10,"5.Convert To Lowercase",13,10,"6.Reverse The String",13,10,"7.Check Whether The Given String Is A Palindrome",13,10,"$"
MSG3 DB "Number Of Vowels: $"
MSG4 DB "Vowels: $"
MSG5 DB "Piglatin: $"
MSG6 DB "Reverse: $"
MSG7 DB "Uppercase: $"
MSG8 DB "Lowercase: $"
MSG9 DB "It Is A Palindrome!$"
MSG10 DB "It Is Not A Palindrome!$" 
CONT DB "Do You Want To Continue?$"
NV DB 0
.CODE
INPUT: 
CALL NEWLINE 
LEA DX,MSG1
MOV AH,9H
INT 21H
LEA DX,STR
MOV AH,0AH
INT 21H 
MENU:
CALL NEWLINE 
 
MOV BX,0 
MOV BL,STR[1]
MOV STRLEN,BX
MOV STR[BX+2],'$'
LEA BX,STR
ADD BX,2
CALL NEWLINE 
 
LEA DX,MSG2
INT 21H
CALL SCAN_NUM
CALL NEWLINE 
MOV AX,0
MOV CHOICE,CX
CMP CX,0
JE INPUT
CMP CX,1
JE ON
CMP CX,2
JE TW 
CMP CX,3
JE TH 
CMP CX,4
JE FO 
CMP CX,5
JE FI
CMP CX,6
JE SIX
CMP CX,7 
JE SE
 
ON:
 MOV CX,STRLEN
 MOV SI,0H
 MOV NV,0
 LOOPONE:
 MOV DL,[BX+SI]
 CMP DL,'A'
 JE INCVOWELS
 CMP DL,'E'
 JE INCVOWELS
 CMP DL,'I'
 JE INCVOWELS
 CMP DL,'O'
 JE INCVOWELS
 CMP DL,'U'
 JE INCVOWELS 
 CMP DL,'a'
 JE INCVOWELS
 CMP DL,'e'
 JE INCVOWELS
 CMP DL,'i'
 JE INCVOWELS
 CMP DL,'o'
 JE INCVOWELS
 CMP DL,'u'
 JE INCVOWELS
 LP1:INC SI
 CMP SI,STRLEN
 JNE LOOPONE
 JE NEXT1
 INCVOWELS:
 INC NV
 JMP LP1 
 NEXT1: 
 CALL NEWLINE
 LEA DX,MSG3
 MOV AH,9H
 INT 21H
 MOV AX,0
 MOV AL,NV
 CALL PRINT_NUM
 JMP CONTI 
 
TW:
 CALL NEWLINE
 LEA DX,MSG4
 MOV AH,9
 INT 21H
 MOV CX,STRLEN
 MOV SI,0H
 LOOPTWO:
 MOV DL,[BX+SI]
 CMP DL,'A'
 JE PRNT
 CMP DL,'E'
 JE PRNT
 CMP DL,'I'
 JE PRNT
 CMP DL,'O'
 JE PRNT
 CMP DL,'U'
 JE PRNT 
 CMP DL,'a'
 JE PRNT
 CMP DL,'e'
 JE PRNT
 CMP DL,'i'
 JE PRNT
 CMP DL,'o'
 JE PRNT
 CMP DL,'u'
 JE PRNT
 JNE INCSI
 PRNT: CALL PRINTVOWELS
 INCSI: INC SI
 CMP SI,STRLEN
 JNE LOOPTWO
 JMP CONTI 
 
TH:
 CALL NEWLINE
 LEA DX,MSG5
 INT 21H
 CMP [BX],'A'
 JE PIGL 
 CMP [BX],'E'
 JE PIGL
 CMP [BX],'I'
 JE PIGL
 CMP [BX],'O'
 JE PIGL
 CMP [BX],'U' 
 JE PIGL 
 CMP [BX],'a'
 JE PIGL 
 CMP [BX],'e'
 JE PIGL
 CMP [BX],'i'
 JE PIGL
 CMP [BX],'o'
 JE PIGL
 CMP [BX],'u' 
 JE PIGL 
 MOV CX,STRLEN
 MOV SI,0H 
 LOOPTHREE:
 CMP [BX+SI],'A'
 JE NEXT4 
 CMP [BX+SI],'E'
 JE NEXT4
 CMP [BX+SI],'I'
 JE NEXT4
 CMP [BX+SI],'O'
 JE NEXT4
 CMP [BX+SI],'U'
 JE NEXT4 
 CMP [BX+SI],'a'
 JE NEXT4 
 CMP [BX+SI],'e'
 JE NEXT4
 CMP [BX+SI],'i'
 JE NEXT4
 CMP [BX+SI],'o'
 JE NEXT4
 CMP [BX+SI],'u'
 JE NEXT4
 INC SI
 INC VOWLOC
 CMP SI,STRLEN
 JE EXIT
 JNE LOOPTHREE
 
 NEXT4:
 LEA DX,STR
 ADD DX,2
 ADD DX,VOWLOC
 MOV AH,9
 INT 21H 
 MOV CX,VOWLOC
 MOV SI,0
 LEA BX,STR
 ADD BX,2
 PIGLOOP:
 MOV DL,[BX+SI]
 MOV AH,2
 INT 21H
 INC SI
 CMP SI,VOWLOC
 JE NEXT5
 JNE PIGLOOP
 
 PIGL:
 LEA DX,STR
 ADD DX,2
 MOV AH,9
 INT 21H 
 
 NEXT5:
 LEA DX,PIG
 MOV AH,9
 INT 21H
 JMP CONTI
 
FO: 
 CALL NEWLINE
 MOV CX,STRLEN
 INC CX 
 MOV SI,0
 LOOPFOUR:
 CMP [BX+SI],'a'
 JL NEXTLETTER
 CMP [BX+SI],'z'
 JG NEXTLETTER 
 SUB [BX+SI],32
 NEXTLETTER:INC SI
 LOOP LOOPFOUR 
 
 LEA DX,MSG7
 MOV AH,9
 INT 21H 
 MOV DX,BX 
 INT 21H
 JMP CONTI 
 
FI: 
 CALL NEWLINE
 MOV CX,STRLEN
 INC CX 
 MOV SI,0
 LOOPFIVE:
 CMP [BX+SI],'A'
 JL NEXTLETTER1
 CMP [BX+SI],'Z'
 JG NEXTLETTER1 
 ADD [BX+SI],32
 NEXTLETTER1:INC SI
 LOOP LOOPFIVE 
 
 LEA DX,MSG8
 MOV AH,9
 INT 21H 
 MOV DX,BX 
 INT 21H
 JMP CONTI 
 
SIX: 
 LEA BX,STR
 ADD BX,2
 CALL NEWLINE
 LEA DX,MSG6
 MOV AH,9H
 INT 21H 
 
 MOV CX,STRLEN
 MOV SI,STRLEN
 DEC SI
 LOOPSIX:
 MOV DL,[BX+SI]
 MOV AH,2
 INT 21H
 DEC SI 
 LOOP LOOPSIX
 JMP CONTI
 
SE:
 CALL NEWLINE
 MOV CX,STRLEN
 LEA BX,STR
 ADD BX,2
 MOV SI,0
 MOV DI,STRLEN
 DEC DI
 LOOPSEVEN:
 MOV DL,[BX+SI]
 CMP DL,[BX+DI]
 JE NEXTLETTER2
 JNE NPAL
 NEXTLETTER2:
 INC SI
 DEC DI
 CMP SI,STRLEN
 JE PAL
 JNE LOOPSEVEN
 
 PAL:
 LEA DX,MSG9
 MOV AH,9
 INT 21H
 JMP CONTI
 
 NPAL:
 LEA DX,MSG10
 MOV AH,9
 INT 21H 
 JMP CONTI
 
CONTI:
CALL NEWLINE
LEA DX,CONT
MOV AH,9
INT 21H
MOV AH,1
INT 21H
CMP AL,'Y'
JE INPUT 
CMP AL,'y'
JE INPUT 
EXIT:
RET
NEWLINE PROC
 LEA DX,NLINE
 MOV AH,9H
 INT 21H 
 RET
NEWLINE ENDP
 
PRINTVOWELS PROC
 MOV AH,2
 INT 21H
 MOV DL,' '
 INT 21H
 RET
PRINTVOWELS ENDP
